let primaryColors = ["red", "yellow", "blue"];
let secondaryColors = ["orange", "green", "violet"];
console.log("Primary Colors: ",primaryColors);
console.log("Secondary Colors: ",secondaryColors);

let allColors = primaryColors.concat(secondaryColors);
console.log("All Colors: ",allColors);

allColors.reverse();
console.log("Reversed Colors: ", allColors);
console.log(allColors);