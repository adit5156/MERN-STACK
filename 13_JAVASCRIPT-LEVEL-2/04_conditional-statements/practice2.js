// Popcorn Management System

let popcornSize = "S";

if(popcornSize === "S") {
    console.log("Total price of popcorn is: 50 rupees");
}
else if (popcornSize === "M") {
    console.log("Total price of popcorn is: 100 rupees");
}
else if(popcornSize === "L") {
    console.log("Total price of popcorn is: 200 rupees");
}
else {
    console.log("Total price of popcorn is: 250 rupees");
}